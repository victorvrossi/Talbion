    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        Categoria selectedItem = (Categoria) cbCategoriaEdit.getSelectedItem();
        Object b = cbSelectLink.getSelectedItem();
        if (b == null) {
            return;
        }
        Link li = (Link) b;
        categoriaMapa.remover(selectedItem, li);
        check();
    }//GEN-LAST:event_btDeleteActionPerformed

    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        Categoria item = (Categoria) cbCategoriaNovo.getSelectedItem();
        Link li = new Link(txNovoTitulo.getText(), txNovoLink.getText());
        item.inserirLink(li);
        check();
    }//GEN-LAST:event_btSaveActionPerformed

    private void btEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEdit1ActionPerformed
        Object b = cbSelectLink.getSelectedItem();
        if (b == null) {
            return;
        }
        Link li = (Link) cbSelectLink.getSelectedItem();
        li.setLink(txEditLink.getText());
        li.setTitulo(txEditTitulo.getText());
        check();
    }//GEN-LAST:event_btEdit1ActionPerformed

