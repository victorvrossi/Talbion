    public void refresh() {
        recriaComboComArrayCategoria(cbCategoriaNovo);
        recriaComboComArrayCategoria(cbCategoriaEdit);
        refreshLink();
        refreshEdit();
    }
    private void recriaComboComArrayCategoria(JComboBox<Categoria> combo){
        combo.setModel(new DefaultComboBoxModel<Categoria>(categoriaMapa.getArray()));
    }

    private List<Link> links;

    private void refreshLink() {
        Object b = cbCategoriaEdit.getSelectedItem();
        if (b == null) {
            return;
        }
        Categoria c = (Categoria) b;        
        links = new ArrayList<>();
        c.percorrerMapaLinks(new CarregaDadosLink() {
            @Override
            public void dadosLink(Link link) {
                links.add(link);
            }
        });
        DefaultComboBoxModel<Link> p = new DefaultComboBoxModel<Link>(links.toArray(new Link[links.size()]));
        cbSelectLink.setModel(p);
        disableEditNoLinkCategoria(c);
    }

    public void refreshEdit() {
        Object b = cbSelectLink.getSelectedItem();
        if (b == null) {
            return;
        }
        Link li = (Link) cbSelectLink.getSelectedItem();
        txEditLink.setText(li.getLink());
        txEditTitulo.setText(li.getTitulo());
    }

    private void check() {
        boolean fecha = !checkJanela.isSelected();
        refresh();
        if (fecha) {
            dispose();
        }
    }

    private void disableEditNoLinkCategoria(Categoria c) {
        if (!c.temLink()) {
            txEditLink.setEnabled(false);
            txEditTitulo.setEnabled(false);
        } else {
            txEditLink.setEnabled(true);
            txEditTitulo.setEnabled(true);
        }
    }

